function readFile(file)
{   
    var f = new XMLHttpRequest();
    f.open("GET", file, false);
    f.onreadystatechange = function ()
    {
        if(f.readyState === 4)
        {
            if(f.status === 200 || f.status == 0)
            {
                res = f.responseText;
            }
        }
    }

    f.send(null);
}

function getData() {
    res = res.split('\n');

    for (i in res){
        if (i == 1) {
            TAMPOP = res[i];
        } else if (i == 2){
            MAX_INFO_LEN = res[i];
        } else if (i>3 && i%4==0) {
            data[aux] = res[i].split(' ');
            aux++;
        } else if (i>3 && i%4==1 + aux1){
            if (aux1 == 0){
                fitness[aux2] = res[i];
                aux1 = 1;
                aux2++;
            } else {
                maxFitness[aux2 - 1] = res[i];
                aux1 = 0;
            }   
        }   
    }
}

function inputData(file) {
    readFile(file);
    getData();
}

