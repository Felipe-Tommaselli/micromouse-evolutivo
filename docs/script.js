function maze (value) {
    index = value;
    ctx.clearRect(0, 0, 640, 640);
    restart(0);
    counter = 0;
    valueAux = 0;
};

function play () {
    flag = 0;
    function loop () {
        document.getElementById("geracao").innerHTML = valueAux;
        document.getElementById("fitMax").innerHTML = maxFitness[valueAux];
        document.getElementById("fit").innerHTML = fitness[valueAux];
        render();
        counter ++;

        move(counter, valueAux);

        let intValue;
        intValue = parseInt(data[valueAux][counter]);
        if (intValue > 3) {
            valueAux += 10;
            restart(1);
        }
        if (flag == 1){
            return;
        }
        setTimeout(loop, 20);
    }
    setTimeout(loop, 20);

    move()
};

function pause () {
    flag = 1;
};

function move() {
    let intValue;

    var LEFT = 0, UP = 2, RIGHT = 1, DOWN = 3;
    intValue = parseInt(data[valueAux][counter]);
    switch(intValue){
        case RIGHT:
            player.x += tileSize;
            break;
        case LEFT:
            player.x -= tileSize;
            break;
        case UP:
            player.y -= tileSize;
            break;
        case DOWN:
            player.y += tileSize;
            break;
    };
};

function restart(i) {
    pause();
    player.x = tileSize*1.1;
    player.y = tileSize*1.1;
    counter = 0;
    render();
    if (i == 0){
        valueAux = 0;
    }
    if (i == 1){
        setTimeout(play, 10);
    }
};

function render() {
    ctx.clearRect(0, 0, WIDTH, HEIGHT);
    ctx.save(); 
    for (var i in maze[index]){
        for (var j in maze[index][i]){
            var tile = maze[index][i][j];
            if (tile === 0){
                var x = j*tileSize;
                var y = i*tileSize;
                ctx.fillRect(x, y, tileSize, tileSize);
            }
        }
    }
    ctx.fillStyle = "#696969";
    ctx.fillRect(player.x, player.y, player.width, player.height);
    ctx.restore();
};

