function play () {
    flag = 0;
    function loop () {
        counter += 0.5;
        render();
        move(counter);
        //console.log(counter);
        if (flag == 1){
            return;
        }
        setTimeout(loop, 50);
    }
    setTimeout(loop, 50);

    move()
};

function pause () {
    flag = 1;
};

function move() {
    var teste = [];
    teste = [3, 3, 3, 3, 3, 2, 2, 3, 1, 1, 3, 2, 3, 3, 1, 1, 1, 1, 1, 1, 3, 3, 2, 2, 0, 0, 1, 1, 3, 3, 2, 3, 0, 1, 2, 3, 2, 3, 0, 1, 0, 1, 2, 4]
    var counterAux = Math.floor(counter);

    var LEFT = 2, UP = 1, RIGHT = 3, DOWN = 0;

    switch(teste[counterAux]){
        case RIGHT:
            player.x += tileSize;
            break;
        case LEFT:
            player.x -= tileSize;
            break;
        case UP:
            player.y -= tileSize;
            break;
        case DOWN:
            player.y += tileSize;
            break;
    }
}

function restart() {
    pause();
    counter = 0;
    player.x = tileSize*1.1;
    player.y = tileSize*1.1;
    render();
}

function render() {
    ctx.clearRect(0, 0, WIDTH, HEIGHT);
    ctx.save(); 
    for (var i in maze[index]){
        for (var j in maze[index][i]){
            var tile = maze[index][i][j];
            if (tile === 0){
                var x = j*tileSize;
                var y = i*tileSize;
                ctx.fillRect(x, y, tileSize, tileSize);
            }
        }
    }
    ctx.fillStyle = "#696969";
    ctx.fillRect(player.x, player.y, player.width, player.height);
    ctx.restore();
}

